# run NodeJs
node

# ctrl + d | quits
# cmd + k | clear console
# tab | show option

# ctrl + c | Quit running program

# initialises a project and creates a package.json file
npm init

# installing dependencies
npm i package-name

# installing dev dependencies
npm i package-name --save-dev
npm i -D package-name

# installing a package globally
npm i package-name --g

# installig a specific package version
npm i package-name@1.0.0

# uninstalling a package
npm uninstall package-name
npm un package-name

# run a file with nodemon
nodemon filename.js

# list of outdated packages
npm outdated

